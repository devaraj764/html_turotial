// JavaScript Operators
// Operators are used to perform operations on variables and values.

// 1. Arithmetic Operators
let a = 10;
let b = 5;

console.log(a + b); // Addition: 15
console.log(a - b); // Subtraction: 5
console.log(a * b); // Multiplication: 50
console.log(a / b); // Division: 2
console.log(a % b); // Modulus: 0
console.log(a ** b); // Exponentiation: 100000

// 2. Comparison Operators
console.log(a > b); // Greater than: true
console.log(a < b); // Less than: false
console.log(a == 10); // Equal to: true
console.log(a === '10'); // Strictly equal: false
console.log(a != b); // Not equal: true
console.log(a !== '10'); // Strictly not equal: true

// 3. Logical Operators
let x = true;
let y = false;
console.log(x && y); // AND: false
console.log(x || y); // OR: true
console.log(!x); // NOT: false
